package com.raj.strings;

public class SwapTwoStrings
{

	public static void main(String[] args) 
	{
		String s1 = "java";
		String s2 = "developer";
		System.out.println(s1);
		System.out.println(s2);
		System.out.println("----------------");
		String s3 = s1;
		s1 = s2;
		s2 = s3;
		System.out.println(s1);
		System.out.println(s2);
	}

}
